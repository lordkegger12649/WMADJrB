using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SimpleVarDemo
{
    class Program
    {
        static void Main(string[] args)
        {
            string msg = "Hello World!";

            Console.WriteLine(msg);
            Console.WriteLine(msg);
            Console.WriteLine(msg);
            Console.WriteLine(msg);
            msg = "I love programming!";
            Console.WriteLine(msg);
            Console.WriteLine(msg);
            Console.WriteLine(msg);

            Console.ReadLine();
        }
    }
}
